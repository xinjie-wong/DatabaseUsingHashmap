    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        switch(selection){
                case "01":
                    if(!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty() && !jTextField3.getText().isEmpty()){
                        databaseSpine.add(jTextField1.getText(), jTextField2.getText(), jTextField3.getText());
                    }else{
                        jTextArea1.setText("");
                        System.out.println("Please fill in all the required inputs.");
                    }
                    break;
                    
                //get
                case "02":
                     if(!jTextField1.getText().isEmpty()){
                        jTextArea1.setText("");
                        databaseSpine.get(jTextField1.getText());                    
                    }else{
                        jTextArea1.setText("");
                        System.out.println("Please fill in the index to retrieve the data.");
                    }
                    break;
                    
                //delete
                case "03":
                    if(!jTextField1.getText().isEmpty()){
                        jTextArea1.setText("");
                        databaseSpine.delete(jTextField1.getText()); 
                        
                        // Clear the table model or update the table content
                        DefaultTableModel modelAfterDelete = new DefaultTableModel();
                        table.setModel(modelAfterDelete);
                    }else{
                        jTextArea1.setText("");
                        System.out.println("Please fill in the index to delete.");
                    }
                    break;  
                    
                //filter
                case "06":
                    if(!jTextField2.getText().isEmpty()){
                        jTextArea1.setText("");
                        
                        // Get the DefaultTableModel from the databaseSpine
                        DefaultTableModel model = databaseSpine.filter(jTextField2.getText());

                         // Set the obtained model to your JTable
                         table.setModel(model);
                    }else{
                        jTextArea1.setText("");
                        System.out.println("Please fill in the datatype to view.");
                    }
                    break;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

