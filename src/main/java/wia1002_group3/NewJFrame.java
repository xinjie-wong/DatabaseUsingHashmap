    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        selection = jTextField4.getText();

        // Check if the text is not empty
        if (!selection.isEmpty()) {
            switch(selection){
                
                //insert
                case "01":
                    jTextField1.setEnabled(true);
                    jTextField2.setEnabled(true);
                    jTextField3.setEnabled(true);
                    jTextArea1.setText("");
                    System.out.println("Please fill in the index, datatype and value.");
                    break;
                    
                //get
                case "02":
                    jTextField1.setEnabled(true);
                    jTextField2.setEnabled(false);
                    jTextField3.setEnabled(false);
                    jTextArea1.setText("");
                    System.out.println("Please fill in the index to retrieve the data.");
                    break;
                    
                //delete
                case "03":
                    jTextField1.setEnabled(true);
                    jTextField2.setEnabled(false);
                    jTextField3.setEnabled(false);
                    jTextArea1.setText("");
                    System.out.println("Please fill in the index to delete the data.");
                    break;
                    
                //clear
                case "04":
                    jTextField1.setEnabled(false);
                    jTextField2.setEnabled(false);
                    jTextField3.setEnabled(false);
                    jTextArea1.setText("");
                    databaseSpine.clear();
                    
                    // Clear the table model
                    DefaultTableModel clearModel = new DefaultTableModel();
                    table.setModel(clearModel);
                    
                    break;
                
                //display
                case "05":
                    jTextField1.setEnabled(false);
                    jTextField2.setEnabled(false);
                    jTextField3.setEnabled(false);
                    jTextArea1.setText("");
                    
                    // Get the DefaultTableModel from the databaseSpine
                    DefaultTableModel model = databaseSpine.display();

                    // Set the obtained model to your JTable
                    table.setModel(model);
                    break;
                    
                //filter
                case "06":
                    jTextField1.setEnabled(false);
                    jTextField2.setEnabled(true);
                    jTextField3.setEnabled(false);
                    jTextArea1.setText("");
                    System.out.println("Please fill in the datatype to view.");
                    break;
                    
                //save
                case "07":
                    jTextField1.setEnabled(false);
                    jTextField2.setEnabled(false);
                    jTextField3.setEnabled(false);
                    jTextArea1.setText("");
                    databaseSpine.save();
                    break;
                   
                //restore
                case "08":
                    jTextField1.setEnabled(false);
                    jTextField2.setEnabled(false);
                    jTextField3.setEnabled(false);
                    jTextArea1.setText("");
                    databaseSpine.restore();
                    break;
                    
                default:
                    jTextArea1.setText("");
                    System.out.println("Please enter a valid selection.");
                    break;
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

